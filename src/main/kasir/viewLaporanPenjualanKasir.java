    private void tabelTransaksiPenjualanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelTransaksiPenjualanMouseClicked
        int i = tabelTransaksiPenjualan.getSelectedRow();
        
        String tempTableIdPenjualan = tabelTransaksiPenjualan.getValueAt(i, 0).toString();
        
        kotakIdPenjualan.setText(tempTableIdPenjualan);
        
        try {
            Koneksi koneksiTampilListDetailTransaksi = new Koneksi();
            String sql = "SELECT "
                    + "id_detail_jual, "
                    + "id_detail_barang, "
                    + "nama_brg, "
                    + "qty, "
                    + "harga, "
                    + "total_harga_barang "
                    + "FROM laporan_detail_penjualan "
                    + "WHERE id_jual = ?";
            PreparedStatement pstmtTampilList = koneksiTampilListDetailTransaksi.prepareStatement(sql);
            pstmtTampilList.setString(1, tempTableIdPenjualan);
            ResultSet rs = pstmtTampilList.executeQuery();
            
            DefaultTableModel tabelListDetailBarang = new DefaultTableModel();
            tabelListDetailBarang.addColumn("ID Detail Transaksi");
            tabelListDetailBarang.addColumn("ID Detail Barang");
            tabelListDetailBarang.addColumn("Nama Barang");
            tabelListDetailBarang.addColumn("Qty");
            tabelListDetailBarang.addColumn("Harga");
            tabelListDetailBarang.addColumn("Total Harga");
            this.tabelListDetailBarang.setModel(tabelListDetailBarang);
            
            while (rs.next()) {                
                tabelListDetailBarang.addRow(new Object[]{
                    rs.getString("id_detail_jual"),
                    rs.getString("id_detail_barang"),
                    rs.getString("nama_brg"),
                    rs.getString("qty"),
                    rs.getString("harga"),
                    rs.getString("total_harga_barang")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error : " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_tabelTransaksiPenjualanMouseClicked

