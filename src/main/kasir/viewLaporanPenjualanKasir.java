    private void dataTabelTransaksi() {
        DefaultTableModel tabelTransaksiPenjualan = new DefaultTableModel();
        tabelTransaksiPenjualan.addColumn("ID Penjualan");
        tabelTransaksiPenjualan.addColumn("Tanggal Transaksi");
        tabelTransaksiPenjualan.addColumn("Total Harga");
        this.tabelTransaksiPenjualan.setModel(tabelTransaksiPenjualan);
        
        try {
            Koneksi koneksiTampilTabel = new Koneksi();

            String sql = "SELECT * FROM laporan_penjualan";

            Statement stm = koneksiTampilTabel.createStatement();
            ResultSet rs = stm.executeQuery(sql);

            while (rs.next()) {
                tabelTransaksiPenjualan.addRow(new Object[]{
                    rs.getString("id_jual"),
                    rs.getString("tanggal"),
                    rs.getString("total_harga_barang")
                });
            }
        } catch (SQLException e) {
            e.getStackTrace();
        }

        
    }
