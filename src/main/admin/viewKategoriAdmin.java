    private void dataTabelKategori() {
        
        DefaultTableModel tabelKategori = new DefaultTableModel();
        tabelKategori.addColumn("ID Kategori");
        tabelKategori.addColumn("Nama Kategori");
        tabelKategori.addColumn("Status");
        this.tabelKategori.setModel(tabelKategori);
        
        try {
            Koneksi koneksiTampilTabel = new Koneksi();
            String sql = "SELECT * FROM kategori";
            Statement stmt = koneksiTampilTabel.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            
            while (rs.next()) {
                tabelKategori.addRow(new Object[] {
                    rs.getString("id_kategori"),
                    rs.getString("nama_kategori"),
                    rs.getString("status")
                });
            }
            
        } catch (Exception e) {
            e.getStackTrace();
        }
        
    }
    
    private void autoIdKategori() {
        try {
            Koneksi koneksiAutoIdUser = new Koneksi();
            String sql = "SELECT id_kategori FROM kategori ORDER BY id_kategori DESC LIMIT 1";

            Statement st = koneksiAutoIdUser.createStatement();
            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                String id_user = rs.getString("id_kategori");
                int nomor = Integer.parseInt(id_user.substring(3));
                nomor++;

                String no_urut = String.format("KTG%03d", nomor);
                kotakIdKategori.setText(no_urut);
            } else {
                kotakIdKategori.setText("KTG001");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal generate ID User!");
        }
    }

