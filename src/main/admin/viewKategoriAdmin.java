    private void tombolSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolSimpanActionPerformed

        String idKategori = kotakIdKategori.getText();
        String namaKategori = kotakNamaKategori.getText();
        String statusKategori = comboStatus.getSelectedItem().toString();

        if (statusKategori.equals("-- Pilih --")) {
            System.out.println("Silahkan pilih statusnya (Aktif/Tidak Aktif)");
            return;
        }
        
        try {
            Koneksi koneksiKategoriSimpan = new Koneksi();
            String sqlInsert = "INSERT INTO kategori VALUES "
                    + "(?, ?, ?)";
            PreparedStatement pstmt = koneksiKategoriSimpan.prepareStatement(sqlInsert);
            pstmt.setString(1, idKategori);
            pstmt.setString(2, namaKategori);
            pstmt.setString(3, statusKategori);
            pstmt.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Berhasil menyimpan kategori "
                    + namaKategori +" ("
                    +idKategori+")");
            System.out.println("ID Kategori: " + idKategori);
            System.out.println("Nama Kategori: " + namaKategori);
            System.out.println("Status: " + statusKategori);
            System.out.println();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error : " + e.getMessage());
            e.printStackTrace();
        }
        dataTabelKategori();
        clearComponents();
    }//GEN-LAST:event_tombolSimpanActionPerformed

