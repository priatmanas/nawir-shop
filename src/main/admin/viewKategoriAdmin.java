    private void tabelKategoriMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelKategoriMouseClicked
        int i = tabelKategori.getSelectedRow();
        
        String tempTableIdKategori = (String) tabelKategori.getValueAt(i, 0);
        String tempTableNamaKategori = (String) tabelKategori.getValueAt(i, 1);
        String tempTableStatusKategori = (String) tabelKategori.getValueAt(i, 2);
        
        kotakIdKategori.setText(tempTableIdKategori);
        kotakNamaKategori.setText(tempTableNamaKategori);
        
        if (tempTableStatusKategori.equals("aktif")) {
            comboStatus.setSelectedIndex(1);
        } else if (tempTableStatusKategori.equals("tidak aktif")) {
            comboStatus.setSelectedIndex(2);
        }

        kotakIdKategori.setEnabled(false);
    }//GEN-LAST:event_tabelKategoriMouseClicked

    private void tombolHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolHapusActionPerformed
        String idKategori = kotakIdKategori.getText();
        String namaKategori = kotakNamaKategori.getText();
        
        try {
            Koneksi koneksiUserHapus = new Koneksi();

            String sqlInsert = "DELETE FROM kategori WHERE "
                    + "id_kategori = ?";

            PreparedStatement pstmt = koneksiUserHapus.prepareStatement(sqlInsert);
            pstmt.setString(1, idKategori);

            int confirmDelete = JOptionPane.showConfirmDialog(this,
                    "Apakah ingin menghapus user dengan nama "
                    + namaKategori + " (" + idKategori + ")?",
                    "Konfirmasi Hapus",
                    JOptionPane.YES_NO_OPTION
            );

            if (confirmDelete == 0) {
                pstmt.executeUpdate();

                JOptionPane.showMessageDialog(this, "Berhasil menghapus " + namaKategori + " ("
                        + idKategori + ")");
                System.out.println("Hapus user dengan id (" + idKategori + ")");
                System.out.println();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error : " + e.getMessage());
            e.printStackTrace();
        }
        dataTabelKategori();
        clearComponents();
    }//GEN-LAST:event_tombolHapusActionPerformed

