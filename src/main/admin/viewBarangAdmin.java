    private void tombolHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolHapusActionPerformed
        String idBarang = kotakIdBarang.getText();
        String namaBarang = kotakNamaBarang.getText();
        
        if (idBarang.isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "ID Barang belum dipilih",
                    "ID Barang Kosong",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        try {
            Koneksi koneksiBarangHapus = new Koneksi();

            String sqlDelete = "DELETE FROM barang WHERE "
                    + "id_barang = ?";

            PreparedStatement pstmt = koneksiBarangHapus.prepareStatement(sqlDelete);
            pstmt.setString(1, idBarang);

            int confirmDelete = JOptionPane.showConfirmDialog(this,
                    "Apakah ingin menghapus barang dengan nama "
                    + namaBarang + " (" + idBarang + ")?",
                    "Konfirmasi Hapus",
                    JOptionPane.YES_NO_OPTION
            );

            if (confirmDelete == 0) {
                pstmt.executeUpdate();

                JOptionPane.showMessageDialog(this, "Berhasil menghapus " + namaBarang + " ("
                        + idBarang + ")");
                System.out.println("Hapus barang dengan id (" + idBarang + ")");
                System.out.println();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error : " + e.getMessage());
            e.printStackTrace();
        }
        dataTabelBarang();
        clearComponents();
    }//GEN-LAST:event_tombolHapusActionPerformed

