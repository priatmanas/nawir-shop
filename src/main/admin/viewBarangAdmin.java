    private void tombolSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolSimpanActionPerformed

        String idBarang = kotakIdBarang.getText();
        String namaBarang = kotakNamaBarang.getText();
        String kategoriBarangDipilih = comboKategori.getSelectedItem().toString();
        
        if (idBarang.isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "ID Barang Kosong",
                    "ID Barang Kosong",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        
        if (namaBarang.isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "Nama barang kosong",
                    "Nama Barang Kosong",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        if (kategoriBarangDipilih.equals("-- Pilih --")) {
            JOptionPane.showMessageDialog(
                    this,
                    "Silahkan pilih kategori",
                    "Pilih kategori",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        try {
            Koneksi koneksiBarangSimpan = new Koneksi();
            String idKategori = "";

            String sqlAmbilKategori = "SELECT id_kategori FROM kategori "
                    + "WHERE nama_kategori = ?";
            PreparedStatement pstmtAmbilKategori
                    = koneksiBarangSimpan.prepareStatement(sqlAmbilKategori);
            pstmtAmbilKategori.setString(1, kategoriBarangDipilih);
            ResultSet rsAmbilKategori = pstmtAmbilKategori.executeQuery();

            if (rsAmbilKategori.next()) {
                idKategori = rsAmbilKategori.getString("id_kategori");
            } else {
                javax.swing.JOptionPane.showMessageDialog(
                        null,
                        "Kategori tidak ditemukan di database!"
                );
                return;
            }

            String sqlInsert = "INSERT INTO barang VALUES "
                    + "(?, ?, ?)";
            PreparedStatement pstmtInsert
                    = koneksiBarangSimpan.prepareStatement(sqlInsert);
            pstmtInsert.setString(1, idBarang);
            pstmtInsert.setString(2, namaBarang);
            pstmtInsert.setString(3, idKategori);
            pstmtInsert.executeUpdate();

            JOptionPane.showMessageDialog(this, "Berhasil menyimpan " + namaBarang + " ("
                    + idBarang + ")");
            System.out.println("ID Barang: " + idBarang);
            System.out.println("Nama Barang: " + namaBarang);
            System.out.println("ID Kategori: " + idKategori + " (" + kategoriBarangDipilih + ")");
            System.out.println();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error : " + e.getMessage());
            e.printStackTrace();
        }
        dataTabelBarang();
        clearComponents();
    }//GEN-LAST:event_tombolSimpanActionPerformed

