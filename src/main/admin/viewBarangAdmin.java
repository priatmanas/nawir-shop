    private void tampilKategori() {
        DefaultComboBoxModel comboKategori = new DefaultComboBoxModel();
        comboKategori.addElement("-- Pilih --");

        try {
            Koneksi koneksiTampilKategori = new Koneksi();
            String sqlKategori = "SELECT nama_kategori FROM kategori "
                    + "WHERE status = 'aktif'";

            Statement stmt = koneksiTampilKategori.createStatement();
            ResultSet rs = stmt.executeQuery(sqlKategori);

            while (rs.next()) {
                comboKategori.addElement(
                        rs.getString("nama_kategori")
                );
                this.comboKategori.setModel(comboKategori);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error : " + e.getMessage());
            e.printStackTrace();
        }
    }

    private void dataTabelBarang() {

        DefaultTableModel tabelBarang = new DefaultTableModel();
        tabelBarang.addColumn("ID Barang");
        tabelBarang.addColumn("Nama Barang");
        tabelBarang.addColumn("Kategori Barang");
        this.tabelBarang.setModel(tabelBarang);

        try {
            Koneksi koneksiTampilTabel = new Koneksi();

            String sql = "SELECT id_barang, nama_brg, nama_kategori "
                    + "FROM barang INNER JOIN kategori "
                    + "ON barang.id_kategori = kategori.id_kategori "
                    + "ORDER BY id_barang ASC";

            Statement stm = koneksiTampilTabel.createStatement();
            ResultSet rs = stm.executeQuery(sql);

            while (rs.next()) {
                tabelBarang.addRow(new Object[]{
                    rs.getString("id_barang"),
                    rs.getString("nama_brg"),
                    rs.getString("nama_kategori")});
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error : " + e.getMessage());
            e.printStackTrace();
        }
    }

    private void autoIdBarang() {
        try {
            Koneksi koneksiAutoIdBarang = new Koneksi();
            String sql = "SELECT id_barang FROM barang ORDER BY id_barang DESC LIMIT 1";

            Statement st = koneksiAutoIdBarang.createStatement();
            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                String id_user = rs.getString("id_barang");
                int nomor = Integer.parseInt(id_user.substring(3));
                nomor++;

                String no_urut = String.format("BRG%04d", nomor);
                kotakIdBarang.setText(no_urut);
            } else {
                kotakIdBarang.setText("BRG0001");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal generate ID Barang!");
        }
    }

