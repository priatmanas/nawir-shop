    private void dataTabelUser() {
        
        DefaultTableModel tabelUser = new DefaultTableModel();
        tabelUser.addColumn("ID");
        tabelUser.addColumn("Nama");
        tabelUser.addColumn("Username");
        tabelUser.addColumn("Role");
        tabelUser.addColumn("Status");
        this.tabelUser.setModel(tabelUser);
        
        try {
            Koneksi koneksiTampilTabel = new Koneksi();
            
            String sql = "SELECT * FROM users";

            Statement stm = koneksiTampilTabel.createStatement();
            ResultSet rs = stm.executeQuery(sql);

            while (rs.next()) {
                tabelUser.addRow(new Object[]{
                    rs.getString("id_user"),
                    rs.getString("nama"),
                    rs.getString("username"),
                    rs.getString("role"),
                    rs.getString("status")});
            }
        } catch (SQLException e) {
            e.getStackTrace();
        }
        
    }
    
    private void autoIdUser() {
        try {
            Koneksi koneksiAutoIdUser = new Koneksi();
            String sql = "SELECT id_user FROM users ORDER BY id_user DESC LIMIT 1";

            Statement stmt = koneksiAutoIdUser.createStatement();
            ResultSet rs = stmt.executeQuery(sql);

            if (rs.next()) {
                String id_user = rs.getString("id_user");
                int nomor = Integer.parseInt(id_user.substring(3));
                nomor++;
                
                String no_urut = String.format("USR%04d", nomor);
                kotakIdUser.setText(no_urut);
            } else {
                kotakIdUser.setText("USR0001");
            }
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal generate ID User!");
        }
    }
    
