    private void tombolSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolSimpanActionPerformed
        
        String idUser = kotakIdUser.getText();
        String namaUser = kotakNamaUser.getText();
        String username = kotakUsername.getText();
        char[] passwordChar = kotakPassword.getPassword();
        String roleUser = comboRole.getSelectedItem().toString();
        String statusUser = comboStatus.getSelectedItem().toString();
        
        if (idUser.isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "Silahkan buat user terlebih dahulu!\n(ID User Kosong)",
                    "Pilih User",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        
        if (namaUser.isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "Nama User tidak boleh kosong!",
                    "Nama User Kosong",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        
        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "Username tidak boleh kosong!",
                    "Username Kosong",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        if (roleUser.equals("-- Pilih --")) {
            JOptionPane.showMessageDialog(
                    this,
                    "Silahkan pilih role",
                    "Pilih Role",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        
        if (statusUser.equals("-- Pilih --")) {
            JOptionPane.showMessageDialog(
                    this,
                    "Silahkan pilih statusnya (Aktif/Tidak Aktif)",
                    "Pilih Status",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        
        try {
            Koneksi koneksiUserSimpan = new Koneksi();

            String sqlInsert = "INSERT INTO users VALUES"
                    + "(?, ?, ?, md5(?), ?, ?)";
            
            PreparedStatement pstmt = koneksiUserSimpan.prepareStatement(sqlInsert);
            pstmt.setString(1, idUser);
            pstmt.setString(2, namaUser);
            pstmt.setString(3, username);
            pstmt.setString(4, new String (passwordChar));
            pstmt.setString(5, roleUser);
            pstmt.setString(6, statusUser);
            
            pstmt.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Berhasil menyimpan "+namaUser+" ("
                    +username+")");
                System.out.println("ID User: "+idUser);
                System.out.println("Nama User: " + namaUser);
                System.out.println("Username: "+username);
                System.out.println("Password: "+passwordChar);
                System.out.println("Role: "+roleUser);
                System.out.println("Status: "+statusUser);
                System.out.println();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error : " + e.getMessage());
            e.printStackTrace();
        }
        dataTabelUser();
        clearComponents();
    }//GEN-LAST:event_tombolSimpanActionPerformed

    private void tabelUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelUserMouseClicked
        int i = tabelUser.getSelectedRow();
        
        String tempTableIdUser = (String) tabelUser.getValueAt(i, 0);
        String tempTableNamaUser = (String) tabelUser.getValueAt(i, 1);
        String tempTableUsername = (String) tabelUser.getValueAt(i, 2);
        String tempTableRoleUser = (String) tabelUser.getValueAt(i, 3);
        String tempTableStatusUser = (String) tabelUser.getValueAt(i, 4);
        
        kotakIdUser.setText(tempTableIdUser);
        kotakNamaUser.setText(tempTableNamaUser);
        kotakUsername.setText(tempTableUsername);
        
        if (tempTableRoleUser.equals("pemilik")) {
            comboRole.setSelectedIndex(1);
        } else if (tempTableRoleUser.equals("kasir")) {
            comboRole.setSelectedIndex(2);
        }
        
        if (tempTableStatusUser.equals("aktif")) {
            comboStatus.setSelectedIndex(1);
        } else if (tempTableStatusUser.equals("tidak aktif")) {
            comboStatus.setSelectedIndex(2);
        }
        kotakIdUser.setEnabled(false);
        kotakPassword.setEnabled(false);
    }//GEN-LAST:event_tabelUserMouseClicked

