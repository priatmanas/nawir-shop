    private void tombolHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolHapusActionPerformed
        String idUser = kotakIdUser.getText();
        String namaUser = kotakNamaUser.getText();
        String username = kotakUsername.getText();
        
        if (idUser.isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "Silahkan pilih user terlebih dahulu!\n(ID User Kosong)",
                    "Pilih User",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        
        try {
            Koneksi koneksiUserHapus = new Koneksi();

            String sqlDelete = "DELETE FROM users WHERE "
                    + "id_user = ?";
            
            PreparedStatement pstmt = koneksiUserHapus.prepareStatement(sqlDelete);
            pstmt.setString(1, idUser);
            
            int confirmDelete = JOptionPane.showConfirmDialog(this,
                    "Apakah ingin menghapus user dengan nama "
                            + namaUser +" ("+username+")?",
                    "Konfirmasi Hapus",
                    JOptionPane.YES_NO_OPTION
            );
            
            if (confirmDelete == 0) {
                pstmt.executeUpdate();

                JOptionPane.showMessageDialog(this, "Berhasil menghapus " + namaUser + " ("
                        + username + ")");
                System.out.println("Hapus user dengan id (" + idUser + ")");
                System.out.println();
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error : " + e.getMessage());
            e.printStackTrace();
        }
        dataTabelUser();
        clearComponents();
    }//GEN-LAST:event_tombolHapusActionPerformed

