    private void tombolHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolHapusActionPerformed
        String idSupplier = kotakIdSupplier.getText();
        String namaSupplier = kotakNamaSupplier.getText();

        try {
            Koneksi koneksiSupplierHapus = new Koneksi();
            String sqlInsert = "DELETE FROM supplier "
                    + "WHERE id_supplier = ?";
            PreparedStatement pstmt = koneksiSupplierHapus.prepareStatement(sqlInsert);
            pstmt.setString(1, idSupplier);
            pstmt.executeUpdate();
            
            int confirmDelete = JOptionPane.showConfirmDialog(this,
                    "Apakah ingin menghapus supplier dengan nama "
                    + namaSupplier + " (" + idSupplier + ")?",
                    "Konfirmasi Hapus",
                    JOptionPane.YES_NO_OPTION
            );

            if (confirmDelete == 0) {
                pstmt.executeUpdate();

                JOptionPane.showMessageDialog(this, "Berhasil menghapus " + namaSupplier + " ("
                        + idSupplier + ")");
                System.out.println("Hapus supplier dengan id (" + idSupplier + ")");
                System.out.println();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error : " + e.getMessage());
            e.printStackTrace();
        }
        dataTabelSupplier();
        clearComponents();
    }//GEN-LAST:event_tombolHapusActionPerformed

