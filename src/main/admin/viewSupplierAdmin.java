    private void tombolSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolSimpanActionPerformed
        String idSupplier = kotakIdSupplier.getText();
        String namaSupplier = kotakNamaSupplier.getText();
        String alamatSupplier = kotakAlamat.getText();
        String teleponSupplier = kotakTelepon.getText();
        String statusSupplier = comboStatus.getSelectedItem().toString();

        if (statusSupplier.equals("-- Pilih --")) {
            JOptionPane.showMessageDialog(
                    this,
                    "Silahkan pilih statusnya (Aktif/Tidak Aktif)",
                    "Pilih Status",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        
        try {
            Koneksi koneksiSupplierSimpan = new Koneksi();
            String sqlInsert = "INSERT INTO supplier VALUES "
                    + "(?, ?, ?, ?, ?)";
            PreparedStatement pstmt = koneksiSupplierSimpan.prepareStatement(sqlInsert);
            pstmt.setString(1, idSupplier);
            pstmt.setString(2, namaSupplier);
            pstmt.setString(3, alamatSupplier);
            pstmt.setString(4, teleponSupplier);
            pstmt.setString(5, statusSupplier);
            pstmt.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Berhasil menyimpan " + namaSupplier + " ("
                    + idSupplier + ")");
            System.out.println("ID Supplier: " + idSupplier);
            System.out.println("Nama Supplier: " + namaSupplier);
            System.out.println("Alamat: " + alamatSupplier);
            System.out.println("Telepon: " + teleponSupplier);
            System.out.println("Status: " + statusSupplier);
            System.out.println();            

            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error : " + e.getMessage());
            e.printStackTrace();
        }
        dataTabelSupplier();
        clearComponents();
    }//GEN-LAST:event_tombolSimpanActionPerformed

    private void tabelSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelSupplierMouseClicked
        int i = tabelSupplier.getSelectedRow();
        
        String tempTableIdSupplier = (String) tabelSupplier.getValueAt(i, 0);
        String tempTableNamaSupplier = (String) tabelSupplier.getValueAt(i, 1);
        String tempTableAlamatSupplier = (String) tabelSupplier.getValueAt(i, 2);
        String tempTableTeleponSupplier = (String) tabelSupplier.getValueAt(i, 3);
        String tempTableStatusSupplier = (String) tabelSupplier.getValueAt(i, 4);
        
        kotakIdSupplier.setText(tempTableIdSupplier);
        kotakNamaSupplier.setText(tempTableNamaSupplier);
        kotakAlamat.setText(tempTableAlamatSupplier);
        kotakTelepon.setText(tempTableTeleponSupplier);
        if (tempTableStatusSupplier.equals("aktif")) {
            comboStatus.setSelectedIndex(1);
        } else if (tempTableStatusSupplier.equals("tidak aktif")) {
            comboStatus.setSelectedIndex(2);
        }
        kotakIdSupplier.setEnabled(false);
    }//GEN-LAST:event_tabelSupplierMouseClicked

