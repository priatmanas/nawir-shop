    private void dataTabelSupplier() {

        DefaultTableModel tabelSupplier = new DefaultTableModel();
        tabelSupplier.addColumn("ID");
        tabelSupplier.addColumn("Nama Supplier");
        tabelSupplier.addColumn("Alamat");
        tabelSupplier.addColumn("Telepon");
        tabelSupplier.addColumn("Status");
        this.tabelSupplier.setModel(tabelSupplier);

        try {
            Koneksi koneksiTampilTabel = new Koneksi();

            String sql = "SELECT * FROM supplier";

            Statement stm = koneksiTampilTabel.createStatement();
            ResultSet rs = stm.executeQuery(sql);

            while (rs.next()) {
                tabelSupplier.addRow(new Object[]{
                    rs.getString("id_supplier"),
                    rs.getString("nama_suppl"),
                    rs.getString("alamat"),
                    rs.getString("telepon"),
                    rs.getString("status")
                });
            }
        } catch (SQLException e) {
            e.getStackTrace();
        }

    }
    
    private void autoIdSupplier() {
        try {
            Koneksi koneksiAutoIdSupplier = new Koneksi();
            String sql = "SELECT id_supplier FROM supplier "
                    + "ORDER BY id_supplier DESC LIMIT 1";

            Statement st = koneksiAutoIdSupplier.createStatement();
            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                String id_user = rs.getString("id_supplier");
                int nomor = Integer.parseInt(id_user.substring(3));
                nomor++;

                String no_urut = String.format("SUP%04d", nomor);
                kotakIdSupplier.setText(no_urut);
            } else {
                kotakIdSupplier.setText("SUP0001");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal generate ID User!");
        }
    }

